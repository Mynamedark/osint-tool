/**
 * MarkdownReporter — Alternative to PDFReporter, outputs .md
 * Same sections: exec summary, categorised tables, risk, audit trail.
 */

'use strict';

const fs   = require('fs');
const path = require('path');

class MarkdownReporter {
  constructor(options = {}) {
    this.outputDir = options.outputDir || './reports';
    if (!fs.existsSync(this.outputDir)) fs.mkdirSync(this.outputDir, { recursive: true });
  }

  generate(investigation, filename = 'osint_report') {
    const outPath = path.join(this.outputDir, `${filename}.md`);
    const inv     = investigation;
    const { target, type, timestamp, totalRaw, confirmedFindings, falsePositivesFiltered } = inv.meta;
    const risk    = inv.riskProfile;
    const reportId = `OSINT-${timestamp.slice(0,10).replace(/-/g,'')}-001`;

    const lines = [];

    // Header
    lines.push(`# OSINT Investigation Report`);
    lines.push(`**Report ID:** \`${reportId}\`  |  **Classification:** TLP:WHITE — Unrestricted`);
    lines.push(`**Target:** ${target}  |  **Type:** ${type}  |  **Date:** ${timestamp.slice(0,10)}`);
    lines.push(`**Analyst:** Dharam Kathiriya (Dark)  |  **Org:** Deveillance`);
    lines.push(``);
    lines.push(`---`);

    // Exec Summary
    lines.push(`## 01 — Executive Summary`);
    lines.push(`Investigation of **"${target}"** (${type}) using passive OSINT — no direct contact.`);
    lines.push(`- Total raw findings collected: **${totalRaw}**`);
    lines.push(`- After entity resolution: **${confirmedFindings} confirmed**, **${falsePositivesFiltered} false positives filtered**`);
    lines.push(`- Overall Risk: **${risk.level}** (${risk.score}/100)`);
    lines.push(``);
    lines.push(risk.signals.length > 0
      ? `Active risk signals: ${risk.signals.map(s => `\`${s.label}\``).join(', ')}`
      : `No material adverse risk signals identified.`);
    lines.push(`\n---`);

    // Findings by category
    const grouped = {};
    for (const f of inv.findings) {
      const cat = f.category || 'uncategorised';
      if (!grouped[cat]) grouped[cat] = [];
      grouped[cat].push(f);
    }

    const catLabels = {
      social:         '02 — Social & Public Footprint',
      infrastructure: '03 — Technical Infrastructure',
      regulatory:     '04 — Contextual & Regulatory',
      uncategorised:  '05 — Uncategorised',
    };

    let sectionNum = 2;
    for (const [cat, findings] of Object.entries(grouped)) {
      if (!findings.length) continue;
      lines.push(`## ${catLabels[cat] || `${sectionNum++} — ${cat}`}`);
      lines.push(`| # | Title | Value | Confidence | Risk Signal | Source | Timestamp |`);
      lines.push(`|---|-------|-------|-----------|-------------|--------|-----------|`);
      findings.forEach((f, i) => {
        const conf = f.confidence != null ? `${Math.round(f.confidence * 100)}%` : '—';
        const url  = f.sourceUrl ? `[link](${f.sourceUrl})` : '—';
        const val  = (f.value || '—').replace(/\|/g, '\\|').slice(0, 80);
        lines.push(`| ${i+1} | ${f.title} | ${val} | ${conf} | ${f.riskSignal || '—'} | ${url} | ${f.timestamp} |`);
      });
      lines.push(``);
    }

    lines.push(`---`);

    // Risk
    lines.push(`## Risk Assessment`);
    lines.push(`### Overall: **${risk.level}** — Score: **${risk.score}/100**`);
    lines.push(``);
    if (risk.signals.length > 0) {
      lines.push(`| Signal | Label | Severity | Weight | Contribution | Source |`);
      lines.push(`|--------|-------|---------|--------|-------------|--------|`);
      for (const s of risk.signals) {
        lines.push(`| ${s.signal} | ${s.label} | **${s.severity}** | ${s.rawWeight} | ${s.contribution} | ${s.source} |`);
      }
    } else {
      lines.push(`No risk signals triggered. All indicators returned clean results.`);
    }
    lines.push(`\n---`);

    // Audit trail
    lines.push(`## Audit Trail`);
    lines.push(`> Every data point with source URL and retrieval timestamp.`);
    lines.push(``);
    lines.push(`| Status | Timestamp | Source | Title | Source URL |`);
    lines.push(`|--------|-----------|--------|-------|------------|`);

    const allFindings = [...inv.findings, ...inv.falsePositives];
    for (const f of allFindings) {
      const status = f.falsePositiveReason ? '⚠️ FP' : '✅';
      const url    = f.sourceUrl || '—';
      lines.push(`| ${status} | ${f.timestamp} | ${f.source} | ${f.title} | ${url} |`);
    }

    lines.push(``);
    lines.push(`---`);
    lines.push(`*Generated by OSINT Tool v1.0 | Dharam Kathiriya (Dark) | ${timestamp}*`);

    fs.writeFileSync(outPath, lines.join('\n'));
    return outPath;
  }
}

module.exports = { MarkdownReporter };
